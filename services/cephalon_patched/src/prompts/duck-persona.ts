/**
 * Parse tick payload and format recent activity for prompts
 */
export function formatRecentActivityForPrompt(
  recentActivity: Array<{ type: string; preview: string; timestamp?: number }> | undefined
): string {
  if (!recentActivity || recentActivity.length === 0) {
    return "Nothing much happening in the stream today.";
  }

  const formatted = recentActivity.slice(0, 5).map(a => 
    `- [${a.type}] ${a.preview}`
  ).join('\n');

  return formatted || "Nothing much happening in the stream today.";
}
